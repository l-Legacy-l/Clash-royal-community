        <h2> Les catégories de deck</h2><br>
        Au fur et à mesure que la méta des arènes et des tournois évolue, plusieurs modèles sont apparus. Il est facile de
        regrouper les decks dans des catégories qui s'appuient sur la même stratégie.

        <h3>Deck Rush</h3><br>
        Les decks rush sont basés sur une seule carte offensive : le cochon ou le géant royale. Généralement, leur coût 
        est assez faible de sorte que la carte offensive revient rapidement dans la main.  La force de ce type de deck 
        est de mettre des dommages sur la tour adverse quasiment à chaque fois que la carte offensive est posée. 
        Néanmoins ce type de deck est facilement contrable : préparez vos cartes pour contrer l'offensive adverse à tous 
        les coups. La seule surprise que vous pouvez avoir est que votre adversaire ait cyclé plus rapidement que vous. 
        Dans ce cas, n'hésitez pas à abandonner votre tour et à tout mettre dans un push sur l'autre ligne.

        <h3>Deck Tank</h3><br>
        Comme son nom l'indique, un deck tank est un deck qui base son offensive sur un tank couvert d'unités de soutien.
        Le golem, le PEKKA, le géant ou encore le molosse sont considérés comme des tanks. Leur force est d'être très 
        difficilement arrêtable lorsque une grosse offensive est lancée. Cependant, la présence d'une tour de l'enfer ou 
        même d'un bâtiment defensif dans le deck adverse rend tout de suite le combat beaucoup plus délicat.  Si vous êtes 
        face à un deck tank, prenez d'abord soin d'éliminer les unités de soutien avant de vous attaquer directement au tank.
        Vous prendrez surement des dommages, mais vous éviterez au moins de perdre votre tour.

        <h3>Deck Grattage</h3><br>
        Les decks gratte sont des decks qui vont jouer sur la longueur de la partie. Petit à petit, ils vont réussir à 
        diminuer les points de vie de la tour adverse jusqu'à ce qu'elle cède. Les cartes qui composent ce type de deck 
        sont souvent le poison, le mineur ou encore la fournaise.  Leur force est d'être difficilement contrable : on 
        enlève facilement quelques centaines de point de vie sans que l'adversaire ne puisse rien faire. Pour contrer ce 
        type de deck, il faut anticiper : garder ses dégats de zone pour désamorcer les combos mineur, anticipier le 
        placement du mineur pour ne pas qu'il frappe la tour, préparez des push lorsque l'adversaire pose une fournaise 
        etc...

        <h3>Deck Setup/Spawer</h3><br>
        L'idée de ce type de deck est de s'installer tout au long de la partie, pour enfin submerger son adversaire. Ces 
        decks sont composés de hutte de gobelins, de huttes de barbares ou encore de fournaises. La version la plus connue
        de ce type de deck est bien évidemment le spawner deck. La force de ce type de deck est bien sûr de lancer une 
        offensive qui submerge totalement l'adversaire. Cependant, si vous ne laissez pas un spawner s'installer (en 
        détruisant immédiatement les bâtiments par exemple) vous pouvez être sûrs de remporter la partie. De plus, les 
        spawners sont sensibles aux decks comportant beaucoup de dommages de zone.

        <h3>Deck Bâtiments</h3><br>
        Ce type de deck se base principalement sur les bâtiments. Cela peut être défensivement comme offensivement. On
        reconnait bien sûr les decks mortiers et arc-x, qui ont reçu un up les rendant de nouveau viables lors de la 
        dernière mise à jour. La force de ce type de deck est de ne pas avoir besoin de sortir de son côté du terrain 
        tout au long de la battle. Il force l'adversaire à changer sa manière de jouer (et notamment de défendre). 
        Cependant, face à des tank, à un cochon ou même un géant royale, il est difficile pour les bâtiments de mettre 
        beaucoup de dommages sur les tours adverses.

