        <h2> Le principe du jeu </h2>
        Le but de Clash Royale est de tomber le plus de tours possibles et (ou) la Tour du Roi de votre adversaire, et ce
        avant le temps imparti, qui est de 3 minutes, en ajoutant 1 minutes (3 pour certains modes) si la partie n'est pas
        départagée. Pour faire match nul, il faut que le même nombre de tours soient tombés (ou non), ou que les Tours du Roi
        tombent en même temps.<br>

        Vous avez le droit à une sélection de 8 cartes, la moitié étant disponibles immédiatement. Chaque fois qu'une 
        carte est placée, la suivante apparaît (voir image) et ainsi de suite. Vous avez le droit à un maximum de 10 
        élixirs, qui se régénère automatiquement, ce dernier aspect étant doublé à partir de la dernière minute de jeu. 
        Le chiffre sur les cartes renseignent sur leur coût en élixir. Chaque victoire vous donne une certaine somme d'or
        (limité à 20x par jour), mais aussi un coffre et un nombre de trophées (que vous pouvez aussi perdre).<br><br>
